class LanguageSwitcher {
    constructor() {
        this.currentLang = localStorage.getItem('preferred-language') || 'en';
        this.init();
    }

    init() {
        console.log('🌐 LanguageSwitcher initialized with language:', this.currentLang);

        // Set initial language
        this.setLanguage(this.currentLang);

        // Add event listeners to language buttons
        const langButtons = document.querySelectorAll('.lang-btn');
        console.log('🔘 Found', langButtons.length, 'language buttons');

        langButtons.forEach(btn => {
            btn.addEventListener('click', (e) => {
                const lang = e.target.dataset.lang;
                console.log('🔄 Language button clicked:', lang);
                this.setLanguage(lang);
            });
        });
    }

    setLanguage(lang) {
        console.log('🌍 Setting language to:', lang);

        // Validate language
        if (!translations[lang]) {
            console.error(`❌ Language ${lang} not found in translations`);
            return;
        }

        this.currentLang = lang;

        // Save to localStorage
        localStorage.setItem('preferred-language', lang);
        console.log('💾 Language saved to localStorage:', lang);

        // Update HTML lang attribute
        document.documentElement.lang = lang;

        // Update active button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
                console.log('✅ Active class added to button:', lang);
            }
        });

        // Update all text with data-i18n attribute
        this.updatePageText();

        // Update Cal.com links with locale
        this.updateCalLinks(lang);

        // Update predictions header link
        this.updatePredictionsLink(lang);

        console.log('✅ Language changed successfully to:', lang);
    }

    // Getter for current language (for other modules)
    get currentLanguage() {
        return this.currentLang;
    }

    updateCalLinks(lang) {
        const callUrl = `/call${lang !== 'en' ? '?lang=' + lang : ''}`;
        document.querySelectorAll('.cal-link').forEach(link => {
            link.href = callUrl;
        });
    }

    updatePredictionsLink(lang) {
        const predictionsUrls = {
            en: 'streaming-predictions-en.html',
            fr: 'streaming-predictions-fr.html',
            zh: 'streaming-predictions-zh.html',
            es: 'streaming-predictions-es.html'
        };
        document.querySelectorAll('.predictions-link').forEach(link => {
            link.href = predictionsUrls[lang] || predictionsUrls.en;
        });
    }

    updatePageText() {
        // Update text content elements
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.dataset.i18n;
            const translation = translations[this.currentLang][key];

            if (translation) {
                // Check if element is an input/textarea
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                } else if (element.tagName === 'OPTION') {
                    element.textContent = translation;
                } else {
                    element.textContent = translation;
                }
            } else {
                console.warn(`Translation not found for key: ${key} in language: ${this.currentLang}`);
            }
        });

        // Update placeholder elements
        const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.dataset.i18nPlaceholder;
            const translation = translations[this.currentLang][key];

            if (translation) {
                element.placeholder = translation;
            }
        });

        // Show/hide language warning for Chinese and Spanish
        const languageWarning = document.getElementById('languageWarning');
        if (languageWarning) {
            if (this.currentLang === 'zh' || this.currentLang === 'es') {
                languageWarning.style.display = 'block';
            } else {
                languageWarning.style.display = 'none';
            }
        }
    }
}

